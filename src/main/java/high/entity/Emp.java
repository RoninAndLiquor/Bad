package high.entity;

public class Emp {

	private String empJobnum;
	private String empName;
	private Integer deptId;
	private String empPhone;
	private Integer postId;
	private String empRemark;
	private Integer titleId;
	private String empIden;
	private Integer empState;
	public String getEmpJobnum() {
		return empJobnum;
	}
	public void setEmpJobnum(String empJobnum) {
		this.empJobnum = empJobnum;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public Integer getDeptId() {
		return deptId;
	}
	public void setDeptId(Integer deptId) {
		this.deptId = deptId;
	}
	public String getEmpPhone() {
		return empPhone;
	}
	public void setEmpPhone(String empPhone) {
		this.empPhone = empPhone;
	}
	public Integer getPostId() {
		return postId;
	}
	public void setPostId(Integer postId) {
		this.postId = postId;
	}
	public String getEmpRemark() {
		return empRemark;
	}
	public void setEmpRemark(String empRemark) {
		this.empRemark = empRemark;
	}
	public Integer getTitleId() {
		return titleId;
	}
	public void setTitleId(Integer titleId) {
		this.titleId = titleId;
	}
	public String getEmpIden() {
		return empIden;
	}
	public void setEmpIden(String empIden) {
		this.empIden = empIden;
	}
	public Integer getEmpState() {
		return empState;
	}
	public void setEmpState(Integer empState) {
		this.empState = empState;
	}
	
}
